package com.mshakir.DataType;

public class MainDataType {

	public static void main(String[] args) {
		System.out.println("Length In Bytes - Different Types:");
		System.out.println("----------------------------------");
		System.out.println("A byte requires ---- " + Byte.BYTES + " -- bytes");
		System.out.println("A Char requires ---- " + Character.BYTES + " -- bytes");
		System.out.println("A Short requires --- " + Short.BYTES + " -- bytes");
		System.out.println("A Int requires ----- " + Integer.BYTES + " -- bytes");
		System.out.println("A Float requires --- " + Float.BYTES + " -- bytes");
		System.out.println("A Long requires ---- " + Long.BYTES + " -- bytes");
		System.out.println("A Double requires -- " + Double.BYTES + " -- bytes");
		System.out.println("----------------------------------");
		System.out.println("Maximum and Minimum values of Different Types: ");
		System.out.println("----------------------------------");
		System.out.println("The minimum Value of a byte is: -- " + Byte.MIN_VALUE);
		System.out.println("The maximum Value of a byte is: -- " + Byte.MAX_VALUE);
		System.out.println("The minimum Value of a Short is: -- " + Short.MIN_VALUE);
		System.out.println("The maximum Value of a Short is: -- " + Short.MAX_VALUE);
		System.out.println("The minimum Value of a Integer is: -- " + Integer.MIN_VALUE);
		System.out.println("The maximum Value of a Integer is: -- " + Integer.MAX_VALUE);
		System.out.println("The minimum Value of a Long is: -- " + Long.MIN_VALUE);
		System.out.println("The maximum Value of a Long is: -- " + Long.MAX_VALUE);

	}

}
